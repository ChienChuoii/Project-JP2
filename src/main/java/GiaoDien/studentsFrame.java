    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String birthday = txtBirthday.getText();
        String gender = cbGender.getSelectedItem().toString();
        String email = txtEmail.getText();
        String address = txtAddress.getText();
        String phoneNumber = txtPhoneNumber.getText();
        String create_at = txtCreate_at.getText();
        String update_at = txtUpdate_at.getText();
        Students std = new Students(0, name, birthday, gender, email, address, phoneNumber, create_at, update_at);

        StudentsDAO.insert(std);

        dataList = StudentsDAO.list("");

        showData();
    }//GEN-LAST:event_btAddActionPerformed

