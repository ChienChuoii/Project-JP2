    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int index = tblStudents.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(rootPane, "Vui long chon sinh vien can xoa");
            return;
        }

        int option = JOptionPane.showConfirmDialog(rootPane, "Ban co chac chan muon xoa sinh vien nay khong?");

        if (option == 0) {
            int student_rollNo = dataList.get(index).getStudent_rollNo();
            StudentsDAO.delete(student_rollNo);

            dataList.remove(index);
            showData();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

