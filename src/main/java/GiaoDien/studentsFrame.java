    private void showData() {
        tableModel.setRowCount(0);

        for (Students student : dataList) {
            tableModel.addRow(new Object[]{
                
                student.getStudent_rollNo(),
                student.getName(),
                student.getBirthday(),
                student.getGender(),
                student.getEmail(),
                student.getAddress(),
                student.getPhoneNumber(),
                student.getCreate_at(),
                student.getUpdate_at()
            });
        }
    }

